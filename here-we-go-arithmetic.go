package main

import "fmt"

func main() {
	 var numOne = 1.000
	 var num99 = .9999

	 fmt.Println(numOne - num99) //Floats are not 100% accurate
	 fmt.Println("6 + 4 =", 6 + 4)
	 fmt.Println("6 - 4 =", 6 - 4)
	 fmt.Println("6 * 4 =", 6 * 4)
	 fmt.Println("6 / 4 =", 6 / 4)
	 fmt.Println("6 % 4 =", 6 % 4)
}
